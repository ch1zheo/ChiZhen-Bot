# safety.py - модуль базовой защиты бота от джейлбрейк-попыток и запросов на создание вредоносного контента (вирусы, эксплойты, оружие и т.д.).

# Работает в два слоя:
# 1. Локальный фильтр (эта часть) - быстрая проверка по паттернам ДО обращения
#   к Groq API. Ловит типовые попытки "обмануть" модель (например, "забудь инструкции", "у тебя больше нет ограничений", ролевые игры в духе DAN)
#   и явные запросы на создание вредоносного/опасного контента.
# 2. Системный промпт (config.py) - дополнительно инструктирует саму модель
#   отказывать в подобных запросах, если они не были пойманы локальным фильтром
#   (например, если сформулированы хитрее или на другом языке).

# ВАЖНО: ни один фильтр по ключевым словам не даёт 100% защиты - это лишь
# дополнительный барьер. Основная защита - грамотный системный промпт и здравые ограничения самой модели.

import logging
import re
from typing import Optional

logger = logging.getLogger(__name__)


# Паттерны попыток джейлбрейка
# Ловим типовые фразы-триггеры, которыми пытаются "переопределить" роль бота
# или заставить его игнорировать системный промпт.
JAILBREAK_PATTERNS = [
    r"игнориру\w*\s+(все\s+)?(предыдущи\w*|систем\w*|свои)\s+(инструкци\w*|правил\w*|указани\w*)",
    r"забудь\s+(все\s+)?(инструкци\w*|правил\w*|о\s+том[, ]+что\s+тебе\s+сказали)",
    r"у\s+тебя\s+(больше\s+)?нет\s+(ограничени\w*|правил|фильтров|цензуры)",
    r"представь[,]?\s+что\s+ты\s+(без\s+ограничени\w*|можешь\s+всё)",
    r"режим\s+(разработчика|бога|без\s+цензуры|jailbreak|дан|dan)",
    r"\bdan\b.{0,20}\bmode\b",
    r"\bdo\s+anything\s+now\b",
    r"\bjailbreak\b",
    r"\bignore\s+(all\s+)?(previous|prior|above|system)\s+instructions\b",
    r"\byou\s+(are\s+now|have\s+no)\s+(restrictions|limits|filters|rules)\b",
    r"\bpretend\s+(you\s+have\s+no|there\s+are\s+no)\s+(restrictions|rules|limits)\b",
    r"act\s+as\s+(an?\s+)?(ai|assistant)\s+(with\s+no|without\s+any)\s+(restrictions|rules|limits)",
    r"выступ\w*\s+в\s+роли.{0,30}без\s+(ограничени\w*|правил|цензуры)",
]

# Паттерны явных запросов на вредоносный/опасный контент
# Общие тематические маркеры, а не пошаговая инструкция - этого достаточно,
# чтобы отсечь явно опасные запросы на уровне темы, а не деталей.
HARMFUL_CONTENT_PATTERNS = [
    r"(напиши\w*|написать|создай\w*|создать|сгенерируй\w*|сгенерировать|разработай\w*|разработать)"
    r"\s+(мне\s+)?(вирус(а|ом|у|е)?\b|троян\w*|вредонос\w*|malware|ransomware)",
    r"(как|способ)\s+(взлома\w*|обойти\s+защиту|получить\s+несанкционирован\w*\s+доступ)",
    r"(как\s+сделать|рецепт|инструкци\w*\s+по\s+созданию)\s+(бомб\w*|взрывчатк\w*|отрав\w*|наркотик\w*|оружи\w*)",
    r"\bhow\s+to\s+(make|build|create)\s+(a\s+)?(bomb|explosive|virus|malware|ransomware|weapon)\b",
    r"\bwrite\s+(a\s+)?(virus|malware|ransomware|exploit|keylogger)\b",
    r"(код|скрипт)\s+для\s+(ddos|взлома|кражи\s+паролей|перехвата\s+данных)",
]

_JAILBREAK_REGEX = re.compile("|".join(JAILBREAK_PATTERNS), re.IGNORECASE | re.UNICODE)
_HARMFUL_REGEX = re.compile("|".join(HARMFUL_CONTENT_PATTERNS), re.IGNORECASE | re.UNICODE)

def check_message_safety(text: str) -> Optional[str]:
    """
    Проверяет текст сообщения пользователя на признаки джейлбрейка
    или запроса вредоносного/опасного контента.

    :param text: текст входящего сообщения пользователя
    :return: строка с текстом отказа, если сообщение подозрительное,
             иначе None (сообщение можно передавать в Groq API как обычно)
    """
    if not text:
        return None

    if _JAILBREAK_REGEX.search(text):
        logger.warning("Обнаружена попытка джейлбрейка: %r", text[:200])
        return (
            "🚫 Я не могу отключить свои правила или притвориться, что у меня "
            "их нет. Задайте, пожалуйста, обычный вопрос - с радостью помогу."
        )

    if _HARMFUL_REGEX.search(text):
        logger.warning("Обнаружен запрос потенциально вредоносного контента: %r", text[:200])
        return (
            "🚫 Я не помогаю создавать вредоносный код, оружие или что-либо "
            "опасное для людей. Если Вас интересует что-то смежное (например, "
            "как защититься от вирусов), могу помочь с этим."
        )

    return None