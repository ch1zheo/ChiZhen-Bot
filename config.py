# config.py - загрузка конфигурации бота из переменных окружения (.env файл).

# Никакие секретные данные (токены, ключи) не хранятся в коде - всё берётся
# из окружения через python-dotenv. Если обязательная переменная отсутствует,
# бот сразу упадёт с понятной ошибкой, а не в середине работы.

import os
from dotenv import load_dotenv

# Загружаем переменные из файла .env, который лежит рядом с проектом.
# Если .env отсутствует - load_dotenv просто ничего не сделает,
# и тогда переменные должны быть заданы иначе (например, в окружении сервера/докера).
load_dotenv()


def _get_env(name: str, required: bool = True, default: str | None = None) -> str:
    """
    Вспомогательная функция для безопасного получения переменной окружения.

    :param name: имя переменной окружения
    :param required: обязательна ли переменная (если да - при отсутствии кидаем ошибку)
    :param default: значение по умолчанию, если переменная не обязательна
    :return: значение переменной окружения
    """
    value = os.getenv(name, default)
    if required and not value:
        raise RuntimeError(
            f"Переменная окружения '{name}' не задана. "
            f"Проверьте файл .env (см. .env.example)."
        )
    return value

